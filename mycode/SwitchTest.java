public class SwitchTest {
    public static void main(String[] args) {
        int a = 3;

switch(a){

            case 1: 
            System.out.println("Cool 1");
            break;
            case 2:
            System.out.println("Cool 2");
            break;
            case 3:
            System.out.println("Cool 3");
            break;
            case 4:
            System.out.println("Not cool");
            break;
            default:
            System.out.println(" Default");



}
            
    }
}