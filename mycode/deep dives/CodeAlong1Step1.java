// This is the class declaration
public class CodeAlong1Step1 {

   // This is the main method, which is the entry point of the program
   public static void main(String[] args) {
       // This line calls the count() method
       count(); //determines the total count of elements
   }

   // This is the count() method declaration
   public static void count() {
       // This is a for loop that iterates from 1 to 10 
       for (int i = 1; i <= 10; i++) {
           // This line prints the value of i to the console
           System.out.println(i);
       }
   }
}
